#include <iostream>
#include <cmath>

using namespace std;

int main()
{
	cout << -11 << "\n" << 10 % 3 << endl;

	int num_1 = 7;
	num_1++;
	cout << num_1 << endl;
	num_1 += 11;
	cout << num_1 << endl;
	num_1 *= 11;
	cout << num_1 << endl;

	cout << pow(2, 3) << endl;
	cout << sqrt(2) << endl;
	cout << fmax(2.2, 3.1) << endl;


	return 0;
}